?>
<div class="<?= Inflector::camel2id(StringHelper::basename($generator->modelClass)) ?>-view">

    <p>
        <?= "<?= " ?>Html::a(<?= $generator->generateString('Back to list') ?>, ['index'], ['class' => 'btn btn-info']) ?>
